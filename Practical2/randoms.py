import random
print(random.randint(5, 20))  # line 1, randomly returns any integer from 5 to 20
# gets any integer from 5 & 20; 5, 20, 11, 15, 20, 17...
print(random.randrange(3, 10, 2))  # line 2, randoms between the range of 3 & 10 with intervals of 2
# gets 3, 5, 7 or 9
print(random.uniform(2.5, 5.5))  # line 3, randoms completely between 2.5 & 5.5
# gets 3.28539279217392, 4.98349263781238, 3.9874268348638237
